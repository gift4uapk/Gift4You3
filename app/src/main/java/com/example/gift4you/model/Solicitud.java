package com.example.gift4you.model;

public class Solicitud {

    private String Parentesco;
    private String Nombre;
    private String Pregunta_Cuatro;
    private String Pregunta_Tres;
    private String Pregunta_Dos;
    private String Pregunta_Uno;
    private String Suma_de_Respuestas;


    public String getParentesco() {
        return Parentesco;
    }

    public void setParentesco(String parentesco) {
        Parentesco = parentesco;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String nombre) {
        Nombre = nombre;
    }

    public String getPregunta_Cuatro() {
        return Pregunta_Cuatro;
    }

    public void setPregunta_Cuatro(String pregunta_Cuatro) {
        Pregunta_Cuatro = pregunta_Cuatro;
    }

    public String getPregunta_Tres() {
        return Pregunta_Tres;
    }

    public void setPregunta_Tres(String pregunta_Tres) {
        Pregunta_Tres = pregunta_Tres;
    }

    public String getPregunta_Dos() {
        return Pregunta_Dos;
    }

    public void setPregunta_Dos(String pregunta_Dos) {
        Pregunta_Dos = pregunta_Dos;
    }

    public String getPregunta_Uno() {
        return Pregunta_Uno;
    }

    public void setPregunta_Uno(String pregunta_Uno) {
        Pregunta_Uno = pregunta_Uno;
    }

    public String getSuma_de_Respuestas() {
        return Suma_de_Respuestas;
    }

    public void setSuma_de_Respuestas(String suma_de_Respuestas) {
        Suma_de_Respuestas = suma_de_Respuestas;
    }
}
